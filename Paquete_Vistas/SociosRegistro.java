    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        String nombre = null, edadS = null, curp = null, rfc = null, idmex = null, ocupacion = null, sexo = null;
        String direccion = null, denominacion = null;
        int edad = 0, idNacionalidad = 0, idRol = 0;
        boolean banderaNombre = true, banderaEdad = true, banderaCurp = true, banderaRfc = true, banderaIdMex = true, banderaOcupacion = true;
        boolean banderaSexo = true, banderaDireccion = true, banderaIdNacionalidad = true, banderaIdRol = true, banderaDenominacion = true;
        
        String errores[] = {" Nombre ", " Edad ", " CURP ", " RFC ", " IDMEX ", " Ocupacion ", " Sexo ", " Dirección ", 
                            " Nacionalidad ", " Rol "};
        String verificar = "Favor de Verificar los siguientes campos: ";
        
        
        nombre = txtNombre.getText();
        if (nombre.isEmpty())
        {
            banderaNombre = false;
            verificar += "-" + errores[0];
        }
        
        edadS = txtEdad.getText();
        try
        {
            edad = Integer.parseInt(edadS);
        }
        catch (NumberFormatException e)
        {
            banderaEdad = false;
            verificar += "-" + errores[1];
        }
        if((edad < 18) || (edad > 100))
        {
            JOptionPane.showMessageDialog(null, "Los socios de una sociedad deben de haber cumplido la mayoría de edad");
            limpiar();
        }
        
        curp = txtCURP.getText();
        if(curp.isEmpty())
        {
            banderaCurp = false;
            verificar += "-" + errores[2];
        }
        
        rfc = txtRfc.getText();
        if(rfc.isEmpty())
        {
            banderaRfc = false;
            verificar += "-" + errores[3];
        }
        
        idmex = txtIdmex.getText();
        if(idmex.isEmpty())
        {
            banderaIdMex = false;
            verificar += "-" + errores[4];
        }
        
        ocupacion = txtOcupacion.getText();
        if(ocupacion.isEmpty())
        {
            banderaOcupacion = false;
            verificar += "-" + errores[5];
        }
        
        if(rbtnMasculino.isSelected())
            
            sexo = "H";
        else
            sexo = "M";
        
        direccion = txtDireccion.getText();
        if(direccion.isEmpty())
        {
            banderaDireccion = false;
            verificar += "-" + errores[7];
        }
        
        idNacionalidad = cbxNacionalidad.getSelectedIndex();
        if(idNacionalidad == 0)
        {
            banderaIdNacionalidad = false;
            verificar += "-" + errores[8];
        }
        
        idRol = cbxRol.getSelectedIndex();
        if(idRol == 0)
        {
            banderaIdRol = false;
            verificar += "-" + errores[9];
        }
        
        int denominacionIndex = cbxDenominacion.getSelectedIndex();
        if(denominacionIndex == 0)
            banderaDenominacion = false;
        else
            denominacion = cbxDenominacion.getSelectedItem().toString();
        
        
        int res = 0;
        
        if(banderaNombre && banderaEdad && banderaCurp && banderaRfc & banderaIdMex && banderaOcupacion && banderaSexo
                && banderaSexo && banderaIdNacionalidad && banderaIdRol && banderaDireccion && banderaDenominacion)
        {
            res = RAS.insertarDatos(nombre, edad, curp, rfc, idmex, ocupacion, sexo, direccion, idNacionalidad, idRol, denominacion);
            
            if(res > 0)
            {
                limpiar();
                listar();
            }
        }
        else
            JOptionPane.showMessageDialog(null, verificar, "Error Registro Socio", 2);   
    }//GEN-LAST:event_btGuardarActionPerformed

