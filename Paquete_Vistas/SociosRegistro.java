    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int res = 0;
        int row = tbSocios.getSelectedRow();
        int fila = tbSocios.getSelectedColumnCount();
        
        if (fila < 1)
            JOptionPane.showMessageDialog(null, "Favor de seleccionar un registro", "Borrar", -1);
        else
        {
            res = RAS.eliminarRegistro(tbSocios.getValueAt(row, 0).toString());
            if (res > 0)
            {
                limpiar();
                listar();
                
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

